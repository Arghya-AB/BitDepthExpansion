class model_func(object):
    """
    Base model function class for processing images and arguments into models
    """
    def __init__(self, details=None) -> None:
        # Method for models to override
        self.details = details
        self.infer = lambda input, image: forward(input, image)

    def __call__(self, image):
        return self.infer(image_names[-1], image)
    
    def cuda(self):
        print("CUDA initialized") 

def preprocess_image(image_path, device):
    """Preprocess image for model inference."""
    image_names.append(image_path)
    img = cv2.imread(image_path, cv2.IMREAD_COLOR)
    img = img.astype(np.float32) / 255.0
    img = torch.from_numpy(np.transpose(img, (2, 0, 1))).float()
    img = img.unsqueeze(0)  # Add batch dimension
    
    if device == 'cuda':
        img = img.cuda()
    return img


